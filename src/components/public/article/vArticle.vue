“勇士去年就已经是历史最佳的队伍之一，”詹皇在赛后采访中说道，“他们在常规赛和季后赛的表现已经证明了一切。之后的休赛期他们得到了杜兰特这样一位能力超强的球员，这让勇士成为了一支更加出色的球队。我们必须找到限制他的方法，虽然这一点肯定很困难。”

这是詹姆斯第二次和杜兰特在总决赛中对决，相比于2012年的稚嫩，如今的KD已经步入生涯巅峰。本场比赛他在38分钟内26投14中，轰下38分8个篮板和8次助攻，没有出现1次失误，相比詹皇的28分15个篮板8次助攻和8次失误，更加高效勇猛。

“第三节是整场比赛的转折点，”詹姆斯说道，“我出现了一些失误，全队有20次失误，送给对手21分，并且让他们在内线得到56分。之前我曾因为失误少而自豪，但今天我的失误太多了。我们防守三分的效果不错，但对手在其它所有方面都比我们出色。”

骑士在总决赛前有长达一周的休息时间，那么他们是否因为长时间没有比赛而丢掉状态了呢？詹姆斯表示：“这和休息、备战时间的长短没关系，只是我们在训练中无法模拟他们的打法。现在经过第一场比赛，我们知道了他们的攻防方式，也犯了一些错误，接下来的系列赛中我们会尽快调整，争取打出更好的表现。”

骑士内线大将乐福今天的表现喜忧参半，喜的是他抢到了季后赛个人新高的21个篮板，同时送出了3次盖帽，忧的是乐福迟迟没能找到手感，进攻端13投4中只得15分。

“我们之后的比赛必须打得更好，”乐福赛后说道，“这场比赛对我们来说非常重要，我们本应该打出更好的表现。我需要得到更多的分数，在攻防两端都能给球队提供帮助。这些数据可能说明不了什么，但比赛录像不会说谎，我们回去会分析录像，从中学习。”

骑士替补射手凯尔-科沃尔同样手感不佳，他在20分钟内4投全失1分未得。赛后科沃尔说道：“我们每个人都有些紧张，错失了一些上篮和投篮，并且不断出现失误。而对手一直保持充沛的活力，打出了自己的风格。在平时我们的终结能力更好，但今天没能体现这一优势。我们会总结今天的比赛，希望在接下来的比赛中能发挥得更好。”
